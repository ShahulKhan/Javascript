/*
Arithmetic Operators :  +  -  *  **  /  %  ++  --
*/

let a = 5
let b = 10

// '+' --> Addition
console.log("a+b = " + (a+b))
console.log()
console.log("--------------------------------------------------------------")

// '-' --> Subraction
console.log("b-a = " + (b-a))
console.log()
console.log("--------------------------------------------------------------")

// '*' --> Multiplication
console.log("a*b = " + (a*b))
console.log()
console.log("--------------------------------------------------------------")

// '/' --> Division (Quotient)
console.log("b/a = " + (b/a))
console.log()
console.log("--------------------------------------------------------------")

// '%' --> Modulus (Reminder)
console.log("b%a = " + (b%a))
console.log()
console.log("--------------------------------------------------------------")

// '++' --> Pre Increment
console.log("++a = " + (++a))
console.log()
console.log("--------------------------------------------------------------")

// '++' --> Post Increment
console.log("b++ = " + (b++))
console.log("b = " + b)
console.log()
console.log("--------------------------------------------------------------")

// '--' --> Pre Decrement
console.log("--a = " + (--a))
console.log("--------------------------------------------------------------")

// '--' --> Post Decrement
console.log("b-- = " + (b--))
console.log("b = " + b)
console.log()
console.log("--------------------------------------------------------------")

// '**' --> Power(Square, Cube)
console.log("a square = " + (a**2))
console.log()
console.log("--------------------------------------------------------------")

console.log("a power 3 = " + (a**3))
console.log("--------------------------------------------------------------")
