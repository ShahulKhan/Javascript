/*
Assignment Operators :  =  +=  -=  *=  /=  %=  **=
*/

// '=' --> Assign a value
let a = 5
console.log("a = " + a)
console.log()
console.log("--------------------------------------------------------------")

// '+=' --> a = a + 1
console.log(a += 1)
console.log()
console.log("--------------------------------------------------------------")

// '-=' --> a = a - 1
console.log(a -= 1)
console.log()
console.log("--------------------------------------------------------------")

// '*=' --> a = a * 3
console.log(a *= 3)
console.log()
console.log("--------------------------------------------------------------")

// '/=' --> a = a / 3  
console.log(a /= 3)
console.log()
console.log("--------------------------------------------------------------")

// '%='  --> a = a % 3
console.log(a %= 3)
console.log()
console.log("--------------------------------------------------------------")

// '**=' --> a = a ** 4
console.log(a **= 2)
console.log("--------------------------------------------------------------")

