/*
Comparison Operators :  ==  ===  !=  !==  >  <  >=  <=  ?
*/

let age = 22

// '>' --> Greater then
console.log(age > 20)
console.log()
console.log("--------------------------------------------------------------")

/*
'>=' --> Grater then or Equal to
Any one condition must be true
*/
console.log(age >= 20)
console.log(age >= 22)
console.log(age >= 25)
console.log()
console.log("--------------------------------------------------------------")

// '<' --> Lesser then
console.log(age < 20)
console.log("--------------------------------------------------------------")

/*
'<=' --> Lesser then or Equal to
Any one condition must be true
*/
console.log(age <= 20)
console.log(age <= 23)
console.log(age <= 22)
console.log()
console.log("--------------------------------------------------------------")



/*
num is a 'String' value and it is equal to 7
*/

let num = "7"
console.log(typeof(num))
console.log("--------------------------------------------------------------")

// '==' --> only compares the values ignores the type
console.log(num == 7)
console.log()
console.log("--------------------------------------------------------------")

// '===' --> compares both the values and type
console.log(num === 7)
console.log()
console.log("--------------------------------------------------------------")

// '!=' --> only checkes the value ignores the type
console.log(num != 23)
console.log(num != 7)
console.log()
console.log("--------------------------------------------------------------")

// '!==' --> checkes both the value and type
console.log(num !== 7)
console.log(num !== 22)
console.log()
console.log("--------------------------------------------------------------")
